turtle.penup()  #pull the pen up
turtle.goto(-50,0)  #set the pen to (-50,0)
turtle.pendown()  #pull the pen down

turtle.forward(200)  #put the pen forward 200 pixels
turtle.right(144)  #turn the pen right 144 degrees

